declare(strict_types=1);

namespace ContaoCommunityAlliance\Merger2\Constraint\Node;

/**
 * Class AndNode.
 */
final class AndNode extends ConjunctionNode
{
    /**
     * {@inheritdoc}
     */
    public function evaluate(): bool
    {
        return $this->left->evaluate() && $this->right->evaluate();
    }
}
